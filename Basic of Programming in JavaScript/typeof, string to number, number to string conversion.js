// typeof operator -- it shows the type of operator

// data types ( primitive datatypes)
// string
// number
// booleans
// undefined
// null
// BigInt
// Symbol

console.log(typeof "Ritik"); // string
console.log(typeof 4); // number
console.log(typeof true); // boolean
console.log(typeof str); // undefined

// number to string
let newNum = 56;
newNum = String(newNum); // use string method
console.log(typeof newNum); // string

// string to number
let newStr = "Ritik"
newStr = Number(newStr); // use number method
console.log(typeof newStr); // number